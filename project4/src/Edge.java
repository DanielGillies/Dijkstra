
public class Edge
	{
	    public final Vertex dest;
	    public final double distance;
	    public Edge next = null;
	    
	    
	    public Edge(Vertex argTarget, double argWeight) {
	    	dest = argTarget; 
	    	distance = argWeight; 
	    }
	    
	    public String toString() {
	    	return this.dest.toString();
	    }
	}